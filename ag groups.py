age=int(input("Enter the age"))
if age>=0 and age<=4:
    print("Kids")
elif age>=5 and age<=9:
    print("child")
elif age>=10 and age<=17:
    print("Adult")
elif age>=18 and age<=21:
    print("mature")
elif age>=22 and age<=24:
    print("people")
elif age>=25 and age<=25:
    print("people")
else:
    print("invalid number")
